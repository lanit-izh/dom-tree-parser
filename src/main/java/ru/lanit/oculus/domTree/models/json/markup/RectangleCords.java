package ru.lanit.oculus.domTree.models.json.markup;

public class RectangleCords {

    private int x_top;
    private int y_top;
    private int x_bottom;
    private int y_bottom;

    public int getX_top() {
        return x_top;
    }

    public void setX_top(int x_top) {
        this.x_top = x_top;
    }

    public int getY_top() {
        return y_top;
    }

    public void setY_top(int y_top) {
        this.y_top = y_top;
    }

    public int getX_bottom() {
        return x_bottom;
    }

    public void setX_bottom(int x_bottom) {
        this.x_bottom = x_bottom;
    }

    public int getY_bottom() {
        return y_bottom;
    }

    public void setY_bottom(int y_bottom) {
        this.y_bottom = y_bottom;
    }

}
